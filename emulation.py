import random

class W1ThermSensor():
    def __init__(self):
        pass

    def get_temperature(self):
        return random.uniform(24.4, 26.9);


class bme280():
    def __init__(self):
        pass

    def get_temperature():
        return random.uniform(24.4, 26.9);

    def get_pressure():
        return random.uniform(902.41, 906.48);

    def get_humidity():
        return random.uniform(20.4, 22.9);